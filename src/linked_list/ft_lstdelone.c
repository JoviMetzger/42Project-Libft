#include <linkedList.h>

/* Takes as a parameter a node and frees the memory of the node’s content 
 * using the function ’del’ given as a parameter and free the node
 */
void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (!lst)
		return ;
	del(lst->content);
	free(lst);
}
